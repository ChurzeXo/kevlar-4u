export async function handleResetPersonas(
  skillsDir: string,
  input: { confirm: boolean }
): Promise<ToolResult> {
  if (!input.confirm) {
    return {
      content: [{ type: "text", text: "⚠️ 恢复操作需要二次确认，请确认你要恢复默认评论员。" }],
      isError: true,
    };
  }

  const results: string[] = [];

  for (const builtin of BUILTIN_PERSONAS) {
    try {
      const meta: PersonaMeta = {
        id: builtin.id,
        name: builtin.name,
        name_en: builtin.name_en,
        version: "1.0.0",
        author: builtin.author,
        tags: builtin.tags,
        description: builtin.description,
      };

      await writePersonaFile(skillsDir, meta, builtin.systemPrompt);
      results.push(`✅ ${builtin.name} 已恢复`);
    } catch (err) {
      const message = err instanceof Error ? err.message : String(err);
      results.push(`❌ ${builtin.name} 恢复失败：${message}`);
    }
  }

  return {
    content: [
      {
        type: "text",
        text: [
          `## 系统人设恢复完成\n`,
          ...results,
          "",
          `💡 现在可以查看评论员列表确认恢复结果。`,
        ].join("\n"),
      },
    ],
  };
}
